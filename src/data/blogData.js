// Fonctions utilitaires pour manipuler les données
export function getArticleById(id) {
  return blogArticles.find(article => article.id === id);
}

export function getArticlesByCategory(category) {
  return blogArticles.filter(article => article.category === category);
}

export function getFeaturedArticles() {
  return blogArticles.filter(article => article.featured);
}

export function getRecentArticles(limit = 6) {
  return blogArticles.slice(0, limit);
}

export function getUniqueCategories() {
  return [...new Set(blogArticles.map(article => article.category))];
}

export function getAllTags() {
  return [...new Set(blogArticles.flatMap(article => article.tags))];
}

export function getSuggestedArticles(currentId, limit = 2) {
  return blogArticles
    .filter(article => article.id !== currentId)
    .slice(0, limit);
}

export function searchArticles(searchTerm) {
  const term = searchTerm.toLowerCase();
  return blogArticles.filter(article => 
    article.title.toLowerCase().includes(term) ||
    article.excerpt.toLowerCase().includes(term) ||
    article.tags.some(tag => tag.toLowerCase().includes(term)) ||
    article.category.toLowerCase().includes(term)
  );
}

export function getArticlesByTag(tag) {
  return blogArticles.filter(article => 
    article.tags.some(t => t.toLowerCase() === tag.toLowerCase())
  );
}

export function paginateArticles(articles, page = 1, perPage = 6) {
  const startIndex = (page - 1) * perPage;
  const endIndex = startIndex + perPage;
  const paginatedArticles = articles.slice(startIndex, endIndex);
  const totalPages = Math.ceil(articles.length / perPage);
  
  return {
    articles: paginatedArticles,
    currentPage: page,
    totalPages,
    hasNextPage: page < totalPages,
    hasPrevPage: page > 1
  };
}