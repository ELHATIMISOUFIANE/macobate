// ===== UTILITAIRES DE BLOG =====
export const blogUtils = {
  // Obtenir toutes les catégories uniques
  getUniqueCategories() {
    return [...new Set(blogArticles.map(article => article.category))];
  },

  // Obtenir tous les tags uniques
  getUniqueTags() {
    return [...new Set(blogArticles.flatMap(article => article.tags))];
  },

  // Obtenir les articles en vedette
  getFeaturedArticles(limit = 3) {
    return blogArticles.filter(article => article.featured).slice(0, limit);
  },

  // Obtenir les articles récents
  getRecentArticles(limit = 6) {
    return blogArticles.slice(0, limit);
  },

  // Obtenir les articles par catégorie
  getArticlesByCategory(category) {
    return blogArticles.filter(article => article.category === category);
  },

  // Obtenir les articles par tag
  getArticlesByTag(tag) {
    return blogArticles.filter(article => article.tags.includes(tag));
  },

  // Rechercher dans les articles
  searchArticles(searchTerm) {
    if (!searchTerm || searchTerm.trim().length < 2) return [];
    
    const term = searchTerm.toLowerCase();
    return blogArticles.filter(article => 
      article.title.toLowerCase().includes(term) ||
      article.excerpt.toLowerCase().includes(term) ||
      article.content.toLowerCase().includes(term) ||
      article.tags.some(tag => tag.toLowerCase().includes(term))
    );
  },

  // Obtenir un article par ID
  getArticleById(id) {
    return blogArticles.find(article => article.id === id);
  },

  // Obtenir les articles avec pagination
  getPaginatedArticles(page = 1, postsPerPage = 6, filters = {}) {
    let filteredArticles = [...blogArticles];
    
    // Appliquer les filtres
    if (filters.category) {
      filteredArticles = filteredArticles.filter(article => 
        article.category === filters.category
      );
    }
    
    if (filters.search) {
      filteredArticles = this.searchArticles(filters.search);
    }
    
    const totalArticles = filteredArticles.length;
    const totalPages = Math.ceil(totalArticles / postsPerPage);
    const startIndex = (page - 1) * postsPerPage;
    const endIndex = startIndex + postsPerPage;
    
    return {
      articles: filteredArticles.slice(startIndex, endIndex),
      totalPages,
      currentPage: page,
      totalArticles,
      hasNextPage: page < totalPages,
      hasPrevPage: page > 1
    };
  },

  // Obtenir les articles similaires (même catégorie, exclut l'article actuel)
  getSimilarArticles(articleId, limit = 3) {
    const currentArticle = this.getArticleById(articleId);
    if (!currentArticle) return [];
    
    return blogArticles
      .filter(article => 
        article.id !== articleId && 
        article.category === currentArticle.category
      )
      .slice(0, limit);
  },

  // Navigation précédent/suivant
  getArticleNavigation(articleId) {
    const currentIndex = blogArticles.findIndex(a => a.id === articleId);
    if (currentIndex === -1) return { prev: null, next: null };
    
    return {
      prev: currentIndex > 0 ? blogArticles[currentIndex - 1] : null,
      next: currentIndex < blogArticles.length - 1 ? blogArticles[currentIndex + 1] : null
    };
  },

  // Compter les articles par catégorie
  getArticleCountByCategory() {
    const categories = this.getUniqueCategories();
    return categories.reduce((acc, category) => {
      acc[category] = this.getArticlesByCategory(category).length;
      return acc;
    }, {});
  },

  // Formater la date
  formatDate(dateString) {
    const options = { 
      year: 'numeric', 
      month: 'long', 
      day: 'numeric' 
    };
    return new Date(dateString).toLocaleDateString('fr-FR', options);
  },

  // Générer un slug à partir d'un texte
  slugify(text) {
    return text
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/[^a-z0-9 -]/g, '')
      .replace(/\s+/g, '-')
      .replace(/-+/g, '-')
      .trim('-');
  }
};

// Export par défaut
export default { blogArticles, blogUtils };