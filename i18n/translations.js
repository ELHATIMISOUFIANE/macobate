// Fonction pour obtenir l'URL dans l'autre langue
export function getAlternateUrl(currentPath, targetLang) {
  const isfrench = currentPath.startsWith('/fr');

  if (targetLang === 'fr' && !isfrench) {
    return '/fr' + currentPath;
  } else if (targetLang === 'en' && isfrench) {
    return currentPath.replace(/^\/fr/, '') || '/';
  }

  return currentPath;
}